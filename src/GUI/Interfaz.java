    private void ModificarTabla(){
        // Deshabilitar la capacidad de mover columnas
        Tablero.getTableHeader().setReorderingAllowed(false);
        // Desactivar la visualización de cabeceras
        Tablero.setTableHeader(null);
        // Establecer el tamaño preferido de la tabla
        Tablero.setPreferredScrollableViewportSize(new Dimension(405, 405));
        //Establecer el tamaño de las filas
        Tablero.setRowHeight(45);
        //Centrar los numeros
        DefaultTableCellRenderer centerRenderer = new DefaultTableCellRenderer();
        centerRenderer.setHorizontalAlignment(JLabel.CENTER);
        for(int i = 0; i<9; i++)
        Tablero.getColumnModel().getColumn(i).setCellRenderer(centerRenderer);
    }
